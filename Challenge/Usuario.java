package Challenge;

public class Usuario {
    
    //atributos
    private String name;
    private int cpf;
    private String sex;
    private String email;
    private String senha;
    
    //construtor
    public Usuario(String name, int cpf, String sex, String email, String senha) {
        this.name = name;
        this.cpf = cpf;
        this.sex = sex;
        this.email = email;
        this.senha = senha;
    }

  

}
